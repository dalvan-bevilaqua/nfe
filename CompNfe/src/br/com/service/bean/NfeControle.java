package br.com.service.bean;

public class NfeControle {
	
	private String cliId;
	private String nfeId;
	private String Id;
	private String dtProc;
	private String impEmiss;
	private String ip;

	public String getCliId() {
		return cliId;
	}

	public void setCliId(String cliId) {
		this.cliId = cliId;
	}

	public String getNfeId() {
		return nfeId;
	}

	public void setNfeId(String nfeId) {
		this.nfeId = nfeId;
	}

	public String getId() {
		return Id;
	}

	public void setId(String id) {
		Id = id;
	}

	public String getDtProc() {
		return dtProc;
	}

	public void setDtProc(String dtProc) {
		this.dtProc = dtProc;
	}

	public String getImpEmiss() {
		return impEmiss;
	}

	public void setImpEmiss(String impEmiss) {
		this.impEmiss = impEmiss;
	}

	public String getIp() {
		return ip;
	}

	public void setIp(String ip) {
		this.ip = ip;
	}

}
